            $('#ctcSubmit').click(function(e) {
                e.preventDefault();

                let ctcForm = new FormData();
                var lastname = $('#surname').val();
                var firstname = $('#firstname').val();
                var middlename = $('#middlename').val();
                var suffix = $('#extension').val();
                var address = $('#address').val();
                var birthdate = $('#birthdate').val();
                var birthplace = $('#birthplace').val();
                var citizenship = $('#citizenship').val();
                var civilStatus = $('#civilStatus').val();
                var gender = $('#gender').val();
                var height = $('#height').val();
                var weight = $('#weight').val();
                var tin = $('#tin').val();
                var occupation = $('#occupation').val();
                var contactNumber = $('#contactNumber').val();
                var icr = $('#icr').val();
                var gross = $('#gross').val();
                var salaries = $('salaries').val();
                var incomeProperty = $('#incomeProperty').val();

                let idFile = $('#idFile')[0].files;
                let incomeFile = $('#incomeFile')[0].files;
                let payslipFile = $('#payslipFile')[0].files;
                let birFile = $('#birFile')[0].files;

                if (lastname == '' || firstname == '' || address == '' || birthdate == '' || birthplace == '' || citizenship == '' || civilStatus == '' || gender == '' || height == '' || weight == '' || tin == '' || occupation == '' || icr == '' || contactNumber == '' || gross == '' || salaries == '' || incomeProperty == '') {
                    Swal.fire({
                        icon: 'warning',
                        title: 'Required Information',
                        text: 'Please input all the required field',
                    })

                } else {

                    if (idFile.length == 0 || incomeFile.length == 0 || payslipFile.length == 0 || birFile.length == 0) {
                        if (idFile.length == 0) {
                            Swal.fire({
                                icon: 'warning',
                                title: 'Government issued valid ID ',
                                text: 'Please provide government issued valid ID',
                            })
                        } else if (incomeFile.length == 0) {
                            Swal.fire({
                                icon: 'warning',
                                title: 'Proof of Income',
                                text: 'Please provide proof of income',
                            })
                        } else if (payslipFile.length == 0) {
                            Swal.fire({
                                icon: 'warning',
                                title: 'Payslip',
                                text: 'Please provide playslip',
                            })
                        } else {
                            Swal.fire({
                                icon: 'warning',
                                title: 'BIR Form 2316',
                                text: 'Please provide BIR Form 2316',
                            })
                        }
                    } else {
                        Swal.fire({
                            icon: 'question',
                            title: 'Data Entry',
                            text: 'Are you sure you want you want to submit this data?',
                            showCancelButton: true,
                            cancelButtonColor: '#6c757d',
                            confirmButtonColor: '#28a745',
                            confirmButtonText: 'Yes',

                        }).then((result) => {
                            if (result.isConfirmed) {

                                ctcForm.append('lastname', lastname);
                                ctcForm.append('firstname', firstname);
                                ctcForm.append('middlename', middlename);
                                ctcForm.append('suffix', suffix);
                                ctcForm.append('address', address);
                                ctcForm.append('birthdate', birthdate);
                                ctcForm.append('birthplace', birthplace);
                                ctcForm.append('citizenship', citizenship);
                                ctcForm.append('civilStatus', civilStatus);
                                ctcForm.append('gender', gender);
                                ctcForm.append('height', height);
                                ctcForm.append('weight', weight);
                                ctcForm.append('tin', tin);
                                ctcForm.append('occupation', occupation);
                                ctcForm.append('icr', icr);
                                ctcForm.append('contactNumber', contactNumber);
                                ctcForm.append('gross', gross);
                                ctcForm.append('salaries', salaries);
                                ctcForm.append('incomeProperty', incomeProperty);
                                ctcForm.append('idFile', idFile[0])
                                ctcForm.append('incomeFile', incomeFile[0])
                                ctcForm.append('payslipFile', payslipFile[0])
                                ctcForm.append('birFile', birFile[0])

                                $.ajax({
                                    type: 'POST',
                                    url: 'php/services/newCtc.php',
                                    data: ctcForm,
                                    contentType: false,
                                    processData: false,
                                    success: function(res) {
                                        const data = JSON.parse(res);
                                        if (data.error != 1) {
                                            var message = data.em
                                            Swal.fire({
                                                icon: 'success',
                                                title: 'Success',
                                                text: message,
                                                showConfirmButton: true,
                                            }).then(() => {
                                                //window.location.reload();
                                            })
                                        } else {
                                            var status = data.status;
                                            var message = data.em;

                                            if (status == 'Pending') {
                                                Swal.fire({
                                                    icon: 'warning',
                                                    title: 'Status Pending',
                                                    html: message,
                                                })
                                            } else {
                                                var approveDate = data.approveDate;
                                                var expireDate = data.expireDate;

                                                Swal.fire({
                                                    icon: 'warning',
                                                    title: 'Status Approved',
                                                    html: '<p>'+message+'<br><br>Approve Date: '+approveDate+'<br>Expired Date: '+expireDate,
                                                })
                                            }
                                        }
                                    }
                                })
                            }
                        })
                    }
                }
            })

